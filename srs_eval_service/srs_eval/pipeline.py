"""
Run AI evaluation metrics on an existing SRS (already generated by the main app).
"""
from __future__ import annotations

import time
import uuid
from typing import Any, Dict, List

from srs_eval.metrics import (
    evaluate_context_understanding,
    evaluate_coherence,
    evaluate_hallucination,
    evaluate_instruction_adherence,
)


def _skipped_metric(key: str, name: str, why_na: str, detail: str) -> Dict[str, Any]:
    return {
        "key": key,
        "name": name,
        "score": None,
        "score_label": "N/A for this check",
        "what_this_means": why_na,
        "how_calculated": detail,
        "highlights": {"warnings": [], "examples": []},
        "skipped": True,
    }


def run_evaluation_on_provided_srs(prompt: str, srs_text: str) -> Dict[str, Any]:
    """
    Evaluate the SRS your model already produced. Uses your requirements text as `prompt`
    (joined processed requirements). Metrics that need multiple fresh generations are marked N/A.
    """
    t0 = time.time()
    prompt = (prompt or "").strip()
    srs_text = (srs_text or "").strip()

    metrics: List[Dict[str, Any]] = []
    metrics.append(evaluate_instruction_adherence(srs_text))
    metrics.append(evaluate_hallucination(prompt, srs_text))
    metrics.append(evaluate_context_understanding(prompt, srs_text))
    metrics.append(evaluate_coherence(srs_text))

    metrics.append(
        _skipped_metric(
            "consistency",
            "Consistency across outputs",
            "This would compare several SRS versions generated from the same input. Here you only have the current document.",
            "Not run: would require multiple full generations. Your score for other metrics still reflects this SRS.",
        )
    )
    metrics.append(
        _skipped_metric(
            "robustness",
            "Robustness",
            "This would compare SRS outputs after slightly rephrasing the prompt. That needs extra model runs beyond this document.",
            "Not run: needs two or more generations with perturbed prompts.",
        )
    )

    run_id = str(uuid.uuid4())
    return {
        "run_id": run_id,
        "prompt": prompt,
        "metrics": metrics,
        "timing_seconds": round(time.time() - t0, 3),
        "mode": "existing_srs",
    }
